void ble_disc_init()
{
 BLE_INFO("BLE DISC INIT");
}

void ble_disc_request(ble_svc_t * p_svc, ble_disc_cb_t * p_cb)
{
  if(!p_svc || !p_cb)
  {
    BLE_ERROR("Invalid parameter");
    return;
  }
  BLE_INFO("Requesting discovery for service: %s", p_svc->svc_name);
}
